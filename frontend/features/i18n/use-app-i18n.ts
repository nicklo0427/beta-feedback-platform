function isAppLocale(value: unknown): value is AppLocale {
  return value === 'zh-TW' || value === 'en'
}

function resolveMessage(locale: AppLocale, key: string): string {
  let current: unknown = MESSAGES[locale]

  for (const segment of key.split('.')) {
    if (!current || typeof current !== 'object' || !(segment in current)) {
      return key
    }

    current = (current as Record<string, unknown>)[segment]
  }

  return typeof current === 'string' ? current : key
}

function interpolateMessage(
  template: string,
  params?: Record<string, string | number>
): string {
  if (!params) {
    return template
  }

  return template.replace(/\{\{(\w+)\}\}/g, (_, key: string) => {
    const value = params[key]
    return value === undefined ? '' : String(value)
  })
}

function applyLocaleToDocument(locale: AppLocale): void {
  if (!import.meta.client) {
    return
  }

  document.documentElement.lang = locale
}

export function translateForLocale(
  locale: AppLocale,
  key: string,
  params?: Record<string, string | number>
): string {
  return interpolateMessage(resolveMessage(locale, key), params)
}

export function useAppLocale() {
  return useState<AppLocale>('app-locale', () => DEFAULT_APP_LOCALE)
}

export function useAppI18n() {
  const locale = useAppLocale()

  return {
    locale,
    t: (key: string, params?: Record<string, string | number>) =>
      translateForLocale(locale.value, key, params)
  }
}

export function setAppLocale(
  nextLocale: AppLocale,
  localeState?: ReturnType<typeof useAppLocale>
): void {
  const locale = localeState ?? useAppLocale()
  locale.value = nextLocale

  if (import.meta.client) {
    window.localStorage.setItem(APP_LOCALE_STORAGE_KEY, nextLocale)
    applyLocaleToDocument(nextLocale)
  }
}

export function useAppLocaleOptions(): LocaleOption[] {
  return [
    {
      value: 'zh-TW',
      label: translateForLocale('zh-TW', 'shell.locale.zhTw')
    },
    {
      value: 'en',
      label: translateForLocale('zh-TW', 'shell.locale.en')
    }
  ]
}

export function useAppLocalePersistence(): void {
  const locale = useAppLocale()
  const hydrated = useState<boolean>('app-locale-hydrated', () => false)
  const watchRegistered = useState<boolean>('app-locale-watch-registered', () => false)

  if (import.meta.client && !hydrated.value) {
    const storedLocale = window.localStorage.getItem(APP_LOCALE_STORAGE_KEY)
    hydrated.value = true
    locale.value = isAppLocale(storedLocale) ? storedLocale : DEFAULT_APP_LOCALE
    applyLocaleToDocument(locale.value)
  }

  if (import.meta.client && !watchRegistered.value) {
    watch(
      locale,
      (nextLocale) => {
        if (!hydrated.value) {
          return
        }

        window.localStorage.setItem(APP_LOCALE_STORAGE_KEY, nextLocale)
        applyLocaleToDocument(nextLocale)
      },
      {
        flush: 'sync'
      }
    )
    watchRegistered.value = true
  }

  useHead(() => ({
    htmlAttrs: {
      lang: locale.value
    }
  }))
}
